CREATE DATABASE my_account;
use my_account;
CREATE TABLE CUSTOMER(cname varchar(15) PRIMARY KEY,city varchar(20) NOT NULL);
CREATE TABLE BRANCH(bname varchar(15) PRIMARY KEY,city varchar(30) CHECK (CITY IN ('NAGPUR','DELHI','BANGALORE','BOMBAY')) NOT NULL);
CREATE TABLE BORROW(loan_no varchar(8) CHECK(loan_no LIKE 'L%') PRIMARY KEY,cname varchar(15) references customer(cname),bname varchar(20)references branch(bname), amount float(8) CHECK (amount>0)NOT NULL); 
CREATE TABLE DEPOSIT (actno varchar(5) CHECK (actno LIKE 'D%') PRIMARY KEY,cname varchar(15) references CUSTOMER(cname),bname VARCHAR(20) references branch(bname),amount FLOAT(8) CHECK (amount>0) NOT NULL,adate DATE);
